              to the analysis of a document and the extraction of any pieces of text that may indicate an obligation -
              "shall", "must", "should" etc. This could be useful in getting a quick overview of the obligations outlined by a document.
            </p>

            <p class="mb-4 text-gray-600">
              <code class="p-1 text-sm text-link bg-secondary">topic classification</code> refers
              to the automatic assessment of a document to determine its topic. With the topic of a document extracted,
