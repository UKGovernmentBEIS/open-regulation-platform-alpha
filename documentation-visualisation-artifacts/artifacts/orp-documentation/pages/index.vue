            </p>

            <p class="mb-4 text-gray-600">
              <code class="p-1 text-sm text-link bg-secondary">named entity extraction</code> refers
              to the analysis of a document and the extraction of explicitly named entities. A "named entity"
              might for example be an agency referenced in a document. We can use these extracted named entities
              to start building relationships between the documents. Futhermore, users will also be able to search for documents
              based on the entities they reference.
            </p>

            <p class="mb-4 text-gray-600">
              Please note that these enrichments are largely automatic at the moment, and therefore should
              not be taken as 100% reliable. We currently have a review system in place for the enrichments as we
